CREATE TABLE Race_Result (
Result_ID  SERIAL PRIMARY KEY,
    Race_ID    INT,
    Driver_ID  INT,
    Rank       INT,
    Time_Taken VARCHAR(20),
    Laps       INT,
    Points     DECIMAL(10,3),
    FOREIGN KEY (Race_ID)   REFERENCES Race_Event(Race_ID),
    FOREIGN KEY (Driver_ID) REFERENCES Driver(Driver_ID)
);

